    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        double total= 0;
        
        if(rCreme.isSelected()) {
            total += 2.0;
        }
        else if(rMorango.isSelected()) {
            total += 2.5;
        }
        else if(rChocolate.isSelected()) {
            total += 5;
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um sabor de sorvete");
        }
        
        if (cbChocolate.isSelected()) {
            total += 1;
        }
        
        if (cbCaramelo.isSelected()) {
            total += 1.5;
        }
        
        if (cbCrocante.isSelected()) {
            total += 2.5;
        }
        
        lblTotal.setText(String.valueOf(total));
        
    }//GEN-LAST:event_jButton2ActionPerformed

