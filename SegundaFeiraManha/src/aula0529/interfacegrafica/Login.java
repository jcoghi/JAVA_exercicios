    private void pswSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pswSenhaKeyPressed

        String nome = txtNome.getText();
        String senha = String.valueOf(pswSenha.getPassword());

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (nome.equals("joao") && senha.equals("123")) {
                ListaTelas novaTela = new ListaTelas();
                novaTela.setVisible(true);
                setVisible(false);
            }else {
            JOptionPane.showMessageDialog(rootPane, "Usu치rio ou senha inv치lidos");
            btnEntrar.setEnabled(false);
            txtNome.setEditable(false);
            pswSenha.setEditable(false);
        }
            
        }
    }//GEN-LAST:event_pswSenhaKeyPressed

