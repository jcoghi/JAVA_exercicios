/*
 * Escrever um programa para exibir os números de 1 até 50 na tela.
 */
package aula0327;

public class Exemplo01 {

    public static void main(String[] args) {
            int i;
        for (i = 1; i < 50; i++) { // i = i + 1
            System.out.println("i = " + i);
        }
        
        System.out.println("i = " + i);
    }
}
